    public function send($customer_id){

        $email = Customer::findOrFail($customer_id)->email;
        $name = Customer::findOrFail($customer_id)->name;

        Mail::to($email)->send(new InvitationMail($name));

        $customers = Customer::all();

        if (request()->wantsJson()) {
            return $customers;
        }
        Session::flash('success','Email Sent!');

        return redirect('/');
    }

